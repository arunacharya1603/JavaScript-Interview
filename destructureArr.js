let arr = [1,2,3,4,5,6,7];

let [n1,n2] = arr;
// This line uses array destructuring to extract the first two elements of the arr array into the variables n1 and n2.
// n1 captures the first element of the arr array.
// n2 captures the second element of the arr array.
console.log(n1);
console.log(n2);

// Output:
// 1
// 2
// The destructuring assignment syntax is a JavaScript expression that makes it possible to unpack values from arrays, or properties from objects, into distinct variables.
